'''3- Calculadora de Volume
* Crie um programa que calcula o volume de uma caixa retangular. 
* Use as seguintes dimensões:  
* Comprimento: 12 cm
* Largura: 14 cm
* Altura: 20 cm'''

Comprimento=12 #cm
Largura=14 #cm
Altura=20  #cm

volume = (Comprimento * Largura * Altura)

print("O volume da caixa retangular é:", volume, "cm³")